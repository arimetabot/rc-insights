def render_html(report: HealthReport) -> str:
    """Render a health report as a styled HTML page."""
    env = Environment(loader=BaseLoader())
    template = env.from_string(HTML_TEMPLATE)
    return template.render(report=report)


def save_report(
    report: HealthReport,
    output_dir: str | Path = ".",
    *,
    formats: list[str] | None = None,
) -> list[Path]:
    """Save report in multiple formats.

    Args:
        report: The health report to save.
        output_dir: Directory to save files in.
        formats: List of formats to generate ("md", "html"). Defaults to both.

    Returns:
        List of generated file paths.
    """
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    formats = formats or ["md", "html"]

    timestamp = report.generated_at.strftime("%Y%m%d_%H%M")
    files: list[Path] = []

    if "md" in formats:
        md_path = output_dir / f"report_{timestamp}.md"
        md_path.write_text(render_markdown(report))
        files.append(md_path)

    if "html" in formats:
        html_path = output_dir / f"report_{timestamp}.html"
        html_path.write_text(render_html(report))
        files.append(html_path)

    return files
