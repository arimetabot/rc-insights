from rc_insights.models import HealthReport

SEVERITY_EMOJI = {
    "critical": "🔴",
    "warning": "🟡",
    "positive": "🟢",
    "info": "🔵",
}

TREND_EMOJI = {
    "up": "📈",
    "down": "📉",
    "stable": "➡️",
}


def render_markdown(report: HealthReport) -> str:
    """Render a health report as Markdown."""
    lines: list[str] = []

    # Header
    lines.append("# 📊 Subscription Health Report")
    lines.append("")
    lines.append(f"**Project:** `{report.project_id}`")
    lines.append(
        f"**Period:** {report.period_start.isoformat()} → {report.period_end.isoformat()}"
    )
    lines.append(f"**Generated:** {report.generated_at.strftime('%Y-%m-%d %H:%M')}")
    lines.append("")

    # Health Score
    score = report.overall_health_score
    bar = "█" * int(score / 5) + "░" * (20 - int(score / 5))
    if score >= 70:
        grade = "Healthy ✅"
    elif score >= 40:
        grade = "Mixed ⚠️"
    else:
        grade = "Critical 🚨"

    lines.append(f"## Health Score: {score:.0f}/100 — {grade}")
    lines.append("```")
    lines.append(f"[{bar}] {score:.0f}%")
    lines.append("```")
    lines.append("")

    # Summary
    lines.append("### Executive Summary")
    lines.append("")
    lines.append(report.summary)
    lines.append("")

    # Overview Metrics
    if report.overview:
        lines.append("## 📋 Key Metrics")
        lines.append("")
        lines.append("| Metric | Value | Period |")
        lines.append("|--------|-------|--------|")
        for m in report.overview.metrics:
            unit = f" {m.unit}" if m.unit and m.unit != "number" else ""
            lines.append(f"| {m.name} | {m.value:,.2f}{unit} | {m.period} |")
        lines.append("")

    # Insights
    if report.insights:
        lines.append("## 🧠 AI Insights")
        lines.append("")

        # Group by severity
        for severity in ["critical", "warning", "positive", "info"]:
            group = [i for i in report.insights if i.severity == severity]
            if not group:
                continue

            emoji = SEVERITY_EMOJI[severity]
            lines.append(f"### {emoji} {severity.title()}")
            lines.append("")

            for insight in group:
                trend = f" {TREND_EMOJI.get(insight.trend or '', '')}" if insight.trend else ""
                metric = f" ({insight.metric_value})" if insight.metric_value else ""
                lines.append(f"#### {insight.title}{metric}{trend}")
                lines.append("")
                lines.append(insight.description)
                lines.append("")
                lines.append(f"> **Recommendation:** {insight.recommendation}")
                lines.append("")

    # Chart Summaries
    if report.charts_data:
        lines.append("## 📈 Charts Summary")
        lines.append("")
        lines.append("| Chart | Latest | Min | Max | Trend |")
        lines.append("|-------|--------|-----|-----|-------|")

        for _name, chart in report.charts_data.items():
            points = chart.data_points
            values = [v for _, v in points if v is not None]
            if not values:
                continue

            latest = f"{values[-1]:,.2f}"
            vmin = f"{min(values):,.2f}"
            vmax = f"{max(values):,.2f}"

            if len(values) >= 7:
                first = sum(values[:7]) / 7
                last = sum(values[-7:]) / 7
                if first > 0:
                    change = ((last - first) / first) * 100
                    trend = f"{'📈' if change > 2 else '📉' if change < -2 else '➡️'} {change:+.1f}%"
                else:
                    trend = "➡️ N/A"
            else:
                trend = "—"

            lines.append(f"| {chart.display_name} | {latest} | {vmin} | {vmax} | {trend} |")

        lines.append("")

    # Footer
    lines.append("---")
    lines.append(
        "*Generated by [RC Insights](https://github.com/arimetabot/rc-insights) — "
        "AI-powered subscription analytics for RevenueCat*"
    )

    return "\n".join(lines)


